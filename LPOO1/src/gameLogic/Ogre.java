package gameLogic;

public class Ogre extends Character {
	private int oi, oj;
	
	public Ogre()
	{
		this.oi = 0;
		this.oj = 0;
		
	}
	
	public int getOi()
	{
		return this.oi;
	}
	
	public int getOj()
	{
		return this.oj;
	}
	
	public void setOi(int a)
	{
		this.oi = a;
	}
	
	public void setOj(int a)
	{
		this.oj = a;
	}
	
	
}
